// ========== НАВИГАЦИЯ ==========
function navigate(pageId, pushState = true) {
  if (pushState) history.pushState({ page: pageId }, '', '#' + pageId);

  renderPage(pageId);
  updateSidebarActive(pageId);
  window.scrollTo({ top: 0, behavior: 'smooth' });
}

// ========== ИНИЦИАЛИЗАЦИЯ ==========
document.addEventListener('DOMContentLoaded', () => {

  // Оглавление книги строится один раз — навигация всегда видна
  rebuildSidebar();

  // Лого/название в сайдбаре — клик возвращает на обложку книги
  const brandLink = document.querySelector('.sidebar__brand');
  if (brandLink) {
    brandLink.addEventListener('click', e => {
      e.preventDefault();
      navigate('home');
      closeMenu();
    });
  }

  // Mobile menu button
  const menuButton = document.getElementById('menuButton');
  const overlay = document.getElementById('overlay');

  if (menuButton) {
    menuButton.addEventListener('click', () => {
      if (document.body.classList.contains('menu-open')) {
        closeMenu();
      } else {
        openMenu();
      }
    });
  }

  if (overlay) {
    overlay.addEventListener('click', closeMenu);
  }

  // Initial page from hash
  const hash = location.hash.replace('#', '');
  if (hash && hash !== 'home' && pages[hash]) {
    navigate(hash, false);
  } else {
    navigate('home', false);
  }
});

